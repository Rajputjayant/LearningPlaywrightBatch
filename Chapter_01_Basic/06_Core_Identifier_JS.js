//Rule of Identifier
//1. Must start with a letter, $ or _ SIGN
//2. Can contain letters, digits, $ and _sign
//3.Cannot start with a number
//4. Cannot be a reserved keyword
//5. Cannot contain space
//6. Cannot contain special characters except $ and _ sign
//7. Case sensitive

var name="Hello";
console.log(name);

var $name="Hello";
console.log($name); 

var _name="Hello";
console.log(_name);

var name1="Hello";
console.log(name1);

var name_1="Hello";
console.log(name_1);

var name$1="Hello";
console.log(name$1);

var name_1$1="Hello";
console.log(name_1$1);

var name_1$1_2="Hello";
console.log(name_1$1_2);


